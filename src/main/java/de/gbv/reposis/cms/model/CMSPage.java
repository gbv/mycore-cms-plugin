package de.gbv.reposis.cms.model;

import java.time.Instant;
import java.util.ArrayList;
import java.util.List;

import jakarta.persistence.CascadeType;
import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.OneToMany;
import jakarta.persistence.OrderBy;
import jakarta.persistence.Table;

/**
 * Entity representing a CMS page.
 */
@Entity
@Table(name = "cms_page")
public class CMSPage {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(nullable = false, unique = true)
    private String slug;

    @Column(name = "created_at", nullable = false)
    private Instant createdAt;

    @Column(name = "updated_at", nullable = false)
    private Instant updatedAt;

    @OneToMany(mappedBy = "page", cascade = CascadeType.ALL, orphanRemoval = true)
    @OrderBy("versionNumber DESC")
    private List<CMSPageVersion> versions = new ArrayList<>();

    public CMSPage() {
    }

    public CMSPage(String slug) {
        this.slug = slug;
        this.createdAt = Instant.now();
        this.updatedAt = Instant.now();
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getSlug() {
        return slug;
    }

    public void setSlug(String slug) {
        this.slug = slug;
    }

    public Instant getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Instant createdAt) {
        this.createdAt = createdAt;
    }

    public Instant getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Instant updatedAt) {
        this.updatedAt = updatedAt;
    }

    public List<CMSPageVersion> getVersions() {
        return versions;
    }

    public void setVersions(List<CMSPageVersion> versions) {
        this.versions = versions;
    }

    public void addVersion(CMSPageVersion version) {
        versions.add(version);
        version.setPage(this);
        this.updatedAt = Instant.now();
    }

    /**
     * Returns the permission ID for this page.
     * Format: page:{slug}
     */
    public String getPermissionId() {
        return "cms:page:" + slug;
    }
}
